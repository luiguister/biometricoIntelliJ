    private void logueo() {

        if (metodos.validarIngreso(txtNombreUsuario.getText(), String.valueOf(txtPassword.getPassword())) == 1) {
            loader.setVisible(true);
            login.setVisible(false);

            // lets add timeout
            new java.util.Timer().schedule(new TimerTask() {
                @Override
                public void run() {
                    //after validating let's show the main Jframe
                    frmPrincipal frm = new frmPrincipal();
                    frm.usuario = txtNombreUsuario.getText();
                    frm.setExtendedState(MAXIMIZED_BOTH);
                    frm.setVisible(true);

                    // after successfull loggin let's close the login window
                    //call:
                    dispose();

                    //
                    //cool stuff.....
                    //Added some Jfree chart so let's see how it goes.
                    //it will include on Github
                }
            }, 1000 * 2);
        } else {
            if (usuarioValido) {
                JOptionPane.showMessageDialog(null, "Acceso denegado:\n"
                                + "Por favor ingrese una contraseña correcta", "Acceso denegado",
                        JOptionPane.ERROR_MESSAGE);
            }else {
                JOptionPane.showMessageDialog(null, "Acceso denegado:\n"
                                + "Por favor ingrese un usuario o contraseña correctos", "Acceso denegado",
                        JOptionPane.ERROR_MESSAGE);
            }



        }

    }

