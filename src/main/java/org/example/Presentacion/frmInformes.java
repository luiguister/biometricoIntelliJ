    int contarAsistencias(String gr, String codigo) {
        String sql = "SELECT COUNT(ea.asistencia) as total"
                + "from grupo as g, estudiante as e, estudiante_asistencia as ea, asistencia as a "
                + "WHERE e.codigo=ea.codigo_estudiante and ea.codigo_asistencia = a.codigo_asistencia "
                + "and a.grupo_codigo=g.codigo and g.codigo=? and e.codigo=?";
        Connection c = con.conectar();

        //Obtiene todas las huellas de la bd
        PreparedStatement buscarStmt;
        try {
            buscarStmt = c.prepareStatement(sql);
            buscarStmt.setString(1, gr);
            buscarStmt.setString(2, codigo);
            ResultSet rs = buscarStmt.executeQuery();
            if (rs.next()) {
                //Lee la plantilla de la base de datos
                return rs.getInt("total");

            }
            rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(frmListadoEstudiante.class.getName()).log(Level.SEVERE, null, ex);
        }
        return 0;
    }

